  <li><strong>முல்லைப்பாட்டு:</strong> பத்துப்பாட்டில் மிகவும் சிறியது (103 அடிகள்). போருக்குச் சென்ற தலைவனுக்காகத் தலைவி காத்திருக்கும் கற்பின் திண்மையைப் பாடுகிறது.</li>
