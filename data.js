  <li><strong>நடுகல்:</strong> ஊரைக் காக்க இறந்த சாதாரண வீரனுக்குக் கல் நட்டு, அவனைத் தெய்வமாக வழிபட்ட மரபைப் புறநானூறு பல இடங்களில் பதிவு செய்துள்ளது.</li>
