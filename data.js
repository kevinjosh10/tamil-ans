  <li><strong>பட்டினப்பாலை:</strong> சோழர்களின் வணிகத் தலைநகரான பூம்புகார் நகரின் பிரம்மாண்டத்தையும், கப்பல் வணிகத்தையும், சுங்க வசூலையும் ஒரு பொருளியல் ஆய்வறிக்கை போலத் தெளிவாக விளக்குகிறது.</li>
