  <li><strong>இரட்டை மீன் சின்னம்:</strong> மறுபுறம் பாண்டியர்களின் அதிகாரப்பூர்வ அரச முத்திரையான 'இரட்டை மீன்' (Two Fishes) உருவம் பொறிக்கப்பட்டிருக்கும்.</li>
