  <li><strong>வரலாற்றாசிரியர் பதிவு:</strong> இதைப் பார்த்த ரோமானிய வரலாற்று அறிஞர் பிளினி (Pliny the Elder), "ரோமானியப் பேரரசின் செல்வங்கள் அனைத்தும் இந்தியாவிற்கு (தமிழகத்திற்கு) வீணாகக் கரைந்து போகிறதே" என்று தனது நூலில் கண்ணீரோடு ஆதங்கப்படுகிறார். இது தமிழர்களின் மாபெரும் சர்வதேச வணிக மேலாதிக்கத்தைக் காட்டுகிறது.</li>
